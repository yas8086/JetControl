//USART1接受中断服务程序
void HAL_UART_RxCpltCallback(UART_HandleTypeDef *huart)
{
	//串口1
	if(huart==&huart1){
		if(USART1_REC_STA<USART1_MAX_RECV_LEN)		//还可以接收数据
		{
			USART1_REC_BUF[USART1_REC_STA] = UART1_temp[0];
			USART1_REC_STA++;
			CntRx1=7; 
		}else{
			USART1_REC_STA|=1<<15;					//强制标记接收完成
			Uart1_ReceiveData_flag = 1;
		}
		HAL_UART_Receive_IT(huart, (uint8_t*)UART1_temp, 1);
		UNUSED(huart);
	}
	//串口2
	else{
		if(USART2_REC_STA<USART2_MAX_RECV_LEN)		//还可以接收数据
		{
			USART2_REC_BUF_Engine[USART2_REC_STA] = UART2_temp[0];
			USART2_REC_STA++;
			CntRx2=7; 
		}else{
			USART2_REC_STA|=1<<15;					//强制标记接收完成
			Uart2_ReceiveData_flag = 1;
		}
		HAL_UART_Receive_DMA(huart, (uint8_t*)UART2_temp, 1);
		UNUSED(huart);
	}
}
